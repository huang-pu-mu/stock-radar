CREATE TABLE IF NOT EXISTS `ai_factor_weight_suggestions` (
  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  `suggestion_date` DATE NOT NULL,
  `factor_key` VARCHAR(40) NOT NULL,
  `factor_name` VARCHAR(80) NOT NULL,
  `current_weight` DECIMAL(8,4) NOT NULL DEFAULT 0,
  `suggested_weight` DECIMAL(8,4) NOT NULL DEFAULT 0,
  `suggestion_action` VARCHAR(20) NOT NULL DEFAULT 'KEEP' COMMENT 'INCREASE / DECREASE / KEEP',
  `confidence_level` VARCHAR(20) NOT NULL DEFAULT 'LOW' COMMENT 'HIGH / MEDIUM / LOW',
  `sample_count` INT NOT NULL DEFAULT 0,
  `success_rate_pct` DECIMAL(10,4) NOT NULL DEFAULT 0,
  `avg_return_5d_pct` DECIMAL(10,4) NULL,
  `reason` VARCHAR(700) NULL,
  `is_applied` TINYINT(1) NOT NULL DEFAULT 0,
